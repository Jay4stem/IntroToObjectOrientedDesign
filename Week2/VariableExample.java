// here is our class called "Varable Example" it is able to be run by other programs and jre
public class VariableExample {

    // the main method - the computer looks for this first!
    public static void main(String[] args) {

        // declare our variables here
        int favNum = 40; // stores our number as an int
        String firstName = "Barb"; // stores the text as a String
        String lastName = "C"; // stores the text as a String

        // Display the message
        System.out.println("Hello " + firstName + " " + lastName + "!!! Your favorite number is " + favNum + ".");
    }
}



